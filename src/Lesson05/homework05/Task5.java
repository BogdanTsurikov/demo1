package Lesson05.homework05;

public class Task5 {
    public int sum(int x, int y) {
        int result = x + y;
        return result;
    }

    public int sub(int x, int y) {
        int result = x - y;
        return result;
    }

    public int multipl(int x, int y) {
        int result = x * y;
        return result;
    }

    public int del(int x, int y) {
        int result = x / y;
        return result;
    }
}


